<h2>Clay</h2>
<p>How to make a very useful material that can be used to make anything you mold it into</p>
<h3>Ingredients:</h3>
<ul>
    <li>Dirt</li>
    <li>Water</li>
    <li>Strainer</li>
    <li>Bucket</li>
</ul>
<p>Dirt: Any dirt near bodys of water will work</p>
<p>Water: Clean water</p>
<p>Strainer: A piece of cloth to seperate organic debris; A second, much thicker cloth (dense towel) to hold the liquid and let it dry out</p>
<p>Bucket: Mix water and dirt 3:1; Hold this liquid inside of the thickest cloth to tie off the top</p>
<b>Notes:</b>
<ul>
    <li>The 3:1 (water to dirt) liquid must dry out inside the thick cloth</li>
    <li>Once the water evaporates from the 3:1 solution, take the clay and mold it into the desired shape</li>
</ul>
