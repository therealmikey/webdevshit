<h2>Sparkling combustion dust</h2>
<p>Using basic gunpowder to make very, very bright flares of white light. This is also good for a "falling star" effect in fireworks/rockets.</p>
<h3>Ingredients:</h3>
<ul>
    <li>Gunpowder</li>
    <li>Magnesium</li>
</ul>
<p>Magnesium: Add in flakes; Magnesium is the silver looking part of flint and steel</p>
<b>Notes:</b>
<ul>
